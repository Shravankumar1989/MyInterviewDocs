# Upper Case
name = "shravankumar patil"
print(name.upper())

# Lower Case
Name = "Shravankumar Patil"
print(Name.lower())

# Remove Whitespace
Name1 = "  Shravankumar Patil  "
print(Name1.strip())

# Replace String
replaceString = "Shravankumar Patil"
print(replaceString.replace("Patil", "P"))

# Split String
value = "Shravankumar Patil"
b = value.split(" ")
print(b)