# Many Values to Multiple Variables
firstname, middlename, lastname = "Shravankumar", "Mallikarjun", "Patil"
print(firstname + " " + middlename + " " + lastname)

# One Value to Multiple Variables
firstname1 = middlename1 = lastname1 = "Shravankumar"
print(firstname1)
print(middlename1)
print(lastname1)

# Unpack a Collection
full_name = ["Shravankumar", "Mallikarjun", "Patil"]
firstname2, middlename2, lastname2 = full_name
print(firstname2)
print(middlename2)
print(lastname2)